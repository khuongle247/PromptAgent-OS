const fs = require("fs");
const path = require("path");

const {
  readJson,
  readText,
  loadSchema,
  validateSchema
} = require("../scripts/validation/validation-utils");
const { buildAgentContext } = require("./agent-runner");
const { writeArtifact } = require("./artifact-store");
const { adaptLegacyArtifact } = require("../adapters/legacy-adapter");
const { retrieveMemories } = require("./memory-retrieval-engine"); // New import for context
const promptVersionManager = require("./prompt-version-manager");

const ROLE_SCHEMA_MAP = {
  planner: "outputs/planner-output.schema.json",
  architect: "outputs/architect-output.schema.json",
  coder: "outputs/coder-output.schema.json",
  reviewer: "outputs/reviewer-output.schema.json",
  debugger: "outputs/debugger-output.schema.json"
};

function readJsonSafe(filePath) {
  try {
    return readJson(filePath);
  } catch (error) {
    return null;
  }
}

function readTextSafe(filePath) {
  try {
    return fs.existsSync(filePath) ? readText(filePath) : "";
  } catch (error) {
    return "";
  }
}

function loadPrompt(rootDir, role) {
  // Phase 9: Try loading from prompt-version-manager first (supports evolution)
  try {
    const active = promptVersionManager.getActiveVersion(role);
    return active.content;
  } catch (e) {
    // Fall back to legacy prompts/{role}.md
    return readTextSafe(path.join(rootDir, "prompts", `${role}.md`));
  }
}

function loadCurrentTask(projectDir) {
  return readJsonSafe(path.join(projectDir, "tasks", "current-task.json"));
}

function resolveTaskId(projectDir, options = {}) {
  if (options.taskId) {
    return options.taskId;
  }

  const currentTask = loadCurrentTask(projectDir);
  if (currentTask && currentTask.taskId) {
    return currentTask.taskId;
  }

  return "TASK-001";
}

function loadProjectBundle(rootDir, projectDir, role) {
  const context = buildAgentContext(rootDir, projectDir, role);
  const taskId = resolveTaskId(projectDir, {});
  const relevantMemories = retrieveMemories(
    rootDir,
    projectDir,
    `context for ${role} task ${taskId}`,
    { limit: 5 }
  );

  return {
    context,
    taskId,
    prompt: loadPrompt(rootDir, role),
    currentTask: loadCurrentTask(projectDir),
    currentTaskId: taskId,
    taskList: readJsonSafe(path.join(projectDir, "tasks", "tasks.json")),
    memorySummary: readJsonSafe(path.join(projectDir, "memory", "memory-summary.json")),
    project: readJsonSafe(path.join(projectDir, "project.json")),
    relevantMemories // New: pass relevant memories to the agent bundle
  };
}

