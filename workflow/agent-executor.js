function projectNameFallback(projectDir) {
  return path.basename(projectDir || process.cwd());
}

function coerceOutput(role, rawOutput, options = {}) {
  if (rawOutput && typeof rawOutput === "object" && !Array.isArray(rawOutput)) {
    if (
      rawOutput.metadata ||
      rawOutput.tasks ||
      rawOutput.decisions ||
      rawOutput.filesChanged ||
      rawOutput.reviewItems
    ) {
      return rawOutput;
    }
  }

  if (typeof rawOutput === "string" || rawOutput === null || rawOutput === undefined) {
    return adaptLegacyArtifact(rawOutput, {
      role,
      taskId: options.taskId,
      title: options.title
    });
  }

  return adaptLegacyArtifact(rawOutput, {
    role,
    taskId: options.taskId,
    title: options.title
  });
}

function validateOutput(rootDir, role, output) {
  const schemaPath = ROLE_SCHEMA_MAP[role];
  if (!schemaPath) {
    return {
      valid: false,
      errors: [`Unknown role: ${role}`],
      schemaPath: null
    };
  }

  const schema = loadSchema(rootDir, schemaPath);
  const result = validateSchema(output, schema);

  return {
    valid: result.valid,
    errors: (result.errors || []).map(error => `${error.instancePath} ${error.message}`.trim()),
    schemaPath
  };
}

async function executeAgent(rootDir, projectDir, role, options = {}) {
  const startTime = Date.now(); // Start timer

  const bundle = loadProjectBundle(rootDir, projectDir, role);
  const prompt = bundle.prompt;
  const taskId = options.taskId || bundle.currentTaskId;
  const promptPath = path.join(rootDir, "prompts", `${role}.md`);

  let rawOutput;
  let executionSource = "mock";

  if (typeof options.llmClient === "function") {
    rawOutput = await options.llmClient({
      role,
      taskId,
      prompt,
      context: bundle.context,
      bundle
    });
    executionSource = options.llmClient.name ? options.llmClient.name : "llm";
  } else {
    rawOutput = buildMockOutput(role, bundle, { ...options, taskId, projectDir });
  }

  const output = coerceOutput(role, rawOutput, {
    taskId,
    title: bundle.currentTask?.title
  });
  const validation = validateOutput(rootDir, role, output);

  let artifact = null;
  if (validation.valid && options.store !== false) {
    artifact = writeArtifact(
      projectDir,
      {
        role,
        taskId,
        output,
        source: executionSource,
        schemaPath: validation.schemaPath
      },
      {
        role,
        taskId,
        source: executionSource,
        schemaPath: validation.schemaPath
      }
    );

    // Emit ArtifactWritten event
    const eventBus = require("./event-bus");
    await eventBus.publish("artifact-written", {
      agent: role,
      taskId,
      artifactPath: artifact.path,
      schemaPath: artifact.schemaPath,
      timestamp: new Date().toISOString(),
      schemaVersion: "1.0"
    });
  }

  // Emit AgentExecuted event
  const eventBus = require("./event-bus");
  await eventBus.publish("agent-executed", {
    agent: role,
    taskId,
    ok: validation.valid,
    output,
    validation,
    duration: Date.now() - startTime, // Use the captured startTime
    timestamp: new Date().toISOString(),
    schemaVersion: "1.0",
    context: bundle.context // Include full context for observability
  });

  return {
    ok: validation.valid,
    role,
    taskId,
    promptPath,
    prompt,
    rawOutput,
    output,
    validation,
    artifact,
    source: executionSource,
    context: bundle.context,
    currentTask: bundle.currentTask,
    project: bundle.project
  };
}

module.exports = {
  executeAgent,
  loadPrompt,
  loadCurrentTask,
  resolveTaskId,
  loadProjectBundle,
  buildMockOutput,
  coerceOutput,
  validateOutput
};

if (require.main === module) {
  const projectName = process.argv[2];
  const role = process.argv[3] || "planner";

  if (!projectName) {
    console.log(
      "Usage: node workflow/agent-executor.js ProjectName [planner|architect|coder|reviewer|debugger]"
    );
    process.exit(1);
  }

  const rootDir = process.cwd();
  const projectDir = path.join(rootDir, "projects", projectName);

  executeAgent(rootDir, projectDir, role)
    .then(result => {
      console.log(JSON.stringify(result, null, 2));
      process.exit(result.ok ? 0 : 1);
    })
    .catch(error => {
      console.error(error.stack || error.message);
      process.exit(1);
    });
}
